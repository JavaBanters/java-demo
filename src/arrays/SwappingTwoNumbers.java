public class SwappingTwoNumbers {

    public static void main(String[] args) {

        int a = 10; //arr[j]
        int b = 20; //arr[j+1]
        int c = 0; // temp
        c = a; //10
        a = b;
        b = c;

        System.out.println("A value " + a);
        System.out.println("B value " + b);

    }

}
